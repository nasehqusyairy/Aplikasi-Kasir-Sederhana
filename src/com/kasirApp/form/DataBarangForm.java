    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        try {
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File file = chooser.getSelectedFile();
        if (file!=null) {
            String ext = FileExtension.get(file);
            if (!ext.toLowerCase().equals(".png")&&!ext.toLowerCase().equals(".jpg")&&!ext.toLowerCase().equals(".jpeg")) {
                throw new Exception("Jenis file tidak didukung!");
            }
            String filename = file.getAbsolutePath();
            foto = filename;
            ImageIcon icon = new ImageIcon(filename);
            Image image = icon.getImage().getScaledInstance(imgPreview.getWidth(), imgPreview.getHeight(), Image.SCALE_SMOOTH);
            imgPreview.setIcon(new ImageIcon(image));
        }
        } catch (Exception err) {
            String msg = err.getMessage()==null?"Terjadi kesalahan!":err.getMessage();
            JOptionPane.showMessageDialog(null, msg, "Error", 0);
        }
    }//GEN-LAST:event_btnUploadActionPerformed

