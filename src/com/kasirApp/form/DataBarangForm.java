    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String[] dataset = {kode,namaBarang,harga,deskripsi,foto};
        int res = 0;
        if (!Editing) {
            res = DataBarangModel.insert(dataset);
        }else{
            res = DataBarangModel.update(dataset);
        }
        if (res==1) {
            model.setDataVector(DataBarangModel.get(), DataBarangModel.TABLE_HEADER);
            setVisible(false);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

