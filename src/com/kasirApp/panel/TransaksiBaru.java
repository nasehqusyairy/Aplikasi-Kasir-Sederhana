    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String kode = inpKode.getText()==null?"":inpKode.getText();
        if (!kode.equals("")) {
            String jumlah = inpJumlah.getText()==null||inpJumlah.getText().equals("")||inpJumlah.getText().equals("0")?"1":inpJumlah.getText();
            String[] dataset = {idTransaksi,kode,jumlah};
            setTotal(model.add(dataset));
            modelTable.setDataVector(model.get(), model.header);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (!kode.equals("")) {
            setTotal(model.delete(kode));
            modelTable.setDataVector(model.get(), model.header);
            kode="";
        }
    }//GEN-LAST:event_btnHapusActionPerformed

