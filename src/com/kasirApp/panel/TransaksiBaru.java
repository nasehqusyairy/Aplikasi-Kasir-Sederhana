    public boolean bayar(){
        String uang = JOptionPane.showInputDialog(null,"Masukkan uang pembayaran","Pembayaran",JOptionPane.QUESTION_MESSAGE);
        boolean status = false;
        try {
            int kembalian = Integer.parseInt(uang)-Integer.parseInt(total);
            if (kembalian<0) {
               JOptionPane.showMessageDialog(null, "Uang pembayaran tidak cukup", "Peringatan", JOptionPane.WARNING_MESSAGE);
               bayar();
            }else{
                JOptionPane.showMessageDialog(null, "Kembalian : "+Integer.toString(kembalian),"Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
                status = true;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pembayaran dibatalkan", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        return status;
    }

